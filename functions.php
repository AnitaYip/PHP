<?php

function add($var1, $var2){
	return $var1+$var2;
}

function subtract($var1, $var2){
	return $var1-$var2;
}
function multiply($var1, $var2){
	return $var1*$var2;
}

function devide($var1, $var2){
	return $var1/$var2;
}

echo add(1,2) . "\n";

echo subtract(1,2) . "\n";

echo multiply(1,2) . "\n";

echo devide(1,2) . "\n";
?>